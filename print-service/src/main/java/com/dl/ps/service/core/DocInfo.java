package com.dl.ps.service.core;

public class DocInfo {
	
	private String docReferenceId;
	private Boolean renderAnnotation;
	
	public String getDocReferenceId() {
		return docReferenceId;
	}
	public void setDocReferenceId(String docReferenceId) {
		this.docReferenceId = docReferenceId;
	}
	public Boolean getRenderAnnotation() {
		return renderAnnotation;
	}
	public void setRenderAnnotation(Boolean renderAnnotation) {
		this.renderAnnotation = renderAnnotation;
	}
	
}
